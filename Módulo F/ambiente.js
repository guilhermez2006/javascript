// O primeiro valor do vetor [] sempre será 0
let num = [2, 3, 4, 6, 7]; // Criando um vetor com números

num.push(19); // Adiciona o valor 19 à última posição do ARRAY

// Algumas propriedades e métodos do ARRAY:
console.log(num.length); // Retorna a quantidade de elementos no ARRAY
console.log(num.sort()); // Organiza os elementos do ARRAY em ordem crescente
console.log(num.indexOf(4)); // Retorna o índice onde o número 4 está localizado no ARRAY